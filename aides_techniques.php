                <h3 tabIndex="0" id="titre3">Choisir son mat&eacute;riel</h3>
                    <p>
                        Voici un guide datant de 2022 pour acc&eacute;der &agrave; des noms de fournisseurs sp&eacute;cialis&eacute;s dans ce type de mat&eacute;riel.
                        Le lien se situe dans la section suivante.
                        Il faut remplir un formulaire et attendre la validation de son compte pour acc&eacute;der au guide.<br>
                        La f&eacute;d&eacute;ration française handisport donne des conseils aussi pour obtenir du mat&eacute;riel.
                        Voici des conseils selon le niveau de pratique.<br>
                        Pour le niveau 1, l’initiation, il est possible d’utilis&eacute; du mat&eacute;riel qui est lou&eacute; ou prêt&eacute; par diff&eacute;rents organismes
                        Ces organismes peuvent être : un club, le comit&eacute; r&eacute;gional handisport ou le comit&eacute; d&eacute;partemental handisport.<br>
                        Le niveau 2 concerne le perfectionnement. Cela permet une pratique avec un mat&eacute;riel individuel, qui est r&eacute;glable.
                        L’objectif est de d&eacute;finir les besoins du joueur avec lui, pour l’aider &agrave; acheter son mat&eacute;riel.<br>
                        Le niveau 3 intervient &agrave; partir de la ma&icirc;trise de ce sport. Cela permet un accompagnement pour acheter du mat&eacute;riel sur mesure.<br>
                        Un fauteuil peut coûter jusqu’&agrave; 10.000 euros.
                    </p>
