                <h3 tabIndex="0" id="titre2">Le fauteuil</h3>
                    <p tabIndex="0">
                        Il y a deux types de fauteuils selon le r&ocirc;le du joueur.
                        Cependant, ils ont des caract&eacute;ristiques communes.<br>
                        La longueur du fauteuil mesure au maximum 46 cm, la hauteur du fauteuil mesure au maximum 43 centim&egrave;tres.<br>
                        Un fauteuil roulant est compos&eacute; de 4 roues, 2 petites &agrave; l’avant et deux principales &agrave; l’arri&egrave;re.
                        Le diam&egrave;tre des roues principales est de maximum 70 centim&egrave;tres, et les deux roues avant doivent être espac&eacute; au maximum de 20 centim&egrave;tres.<br>
                        Le fauteuil doit comporter un dispositif anti-bascule. Il est accroch&eacute; &agrave; l’arri&egrave;re du fauteuil.
                        Cependant, le dispositif anti-bascul ne peut pas être &agrave; ressort.<br>
                        Les fauteuils des attaquants int&egrave;grent &eacute;galement des ailes tandis que les fauteuils des d&eacute;fenseurs int&egrave;grent &eacute;galement un pare-chocs.<br>
                        Les parts-chocs sont accroch&eacute;s &agrave; l’avant du fauteuil, une barre relie le pare-chocs au châssis du fauteuil.<br>
                        Les ailes du fauteuil sont situ&eacute;es entre l’avant du fauteuil et les roues principales.<br>
                    </p>
